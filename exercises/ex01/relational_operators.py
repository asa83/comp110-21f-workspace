num1: int = int(input("Left-hand side: "))
num2: int = int(input("Right-hand side: "))
print(str(num1) + " < " + str(num2) + " is " + str(num1 < num2))
print(str(num1) + " >= " + str(num2) + " is " + str(num1 >= num2))
print(str(num1) + " == " + str(num2) + " is " + str(num1 == num2))
print(str(num1) + " != " + str(num2) + " is " + str(num1 != num2))